class ButtonDings extends StatefulWidget {
  String label;
  VoidCallback? onPressed;
  ButtonDings({Key? key, required this.label, this.onPressed})
      : super(key: key);

  @override
  State<ButtonDings> createState() => _ButtonDingsState();
}

class _ButtonDingsState extends State<ButtonDings> {
  double height = 60;
  double width = 200;
  double textSize = 20;
  @override
  Widget build(BuildContext context) {
    return SizedBox(
      height: 65,
      width: 205,
      child: Center(
        child: MouseRegion(
          cursor: SystemMouseCursors.click,
          onEnter: (event) {
            setState(() {
              height = height - 60 / 35;
              width = width - 200 / 35;
              textSize = textSize - 20 / 35;
            });
          },
          onExit: (event) {
            setState(() {
              height = 60;
              width = 200;

              textSize = 20;
            });
          },
          child: GestureDetector(
              onTap: widget.onPressed,
              child: AnimatedContainer(
                duration: const Duration(milliseconds: 100),
                width: width,
                height: height,
                decoration: BoxDecoration(
                  borderRadius: BorderRadius.circular(50.0),
                  color: const Color(0xFFEF4136),
                ),
                child: Center(
                    child: SizedBox(
                  child: AnimatedDefaultTextStyle(
                    style: GoogleFonts.rubik(
                      fontSize: textSize,
                      color: Colors.white,
                      fontWeight: FontWeight.w600,
                    ),
                    duration: const Duration(milliseconds: 100),
                    child: Text(widget.label),
                  ),
                )),
              )),
        ),
      ),
    );
  }
}

